#include "parse.h"

int cmd_size(t_cmd *cmd)
{
	t_cmd *tmp;
	int i;

	i = 0;
	tmp = cmd;
	while (tmp != NULL)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
