#include "parse.h"

void free_cmd(t_cmd *cmd)
{
	t_cmd *tmp;

	while (cmd != NULL)
	{
		tmp = cmd;
		cmd = cmd->next;
		free(tmp);
	}
	cmd = NULL;
}
